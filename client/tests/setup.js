import { jest } from '@jest/globals';
import '@testing-library/jest-dom';

// Global test setup for client tests

// Mock console methods for cleaner test output
global.console = {
  ...console,
  log: jest.fn(),
  info: jest.fn(),
  warn: jest.fn(),
  error: jest.fn()
};

// Mock ResizeObserver
